/** All distinct families for grouping */
export function getModelFamilies(): string[] {
  const families = new Set(MODEL_CATALOG.map((m) => m.family));
  return [...families].sort();
}

/** Filter catalog by category, tier, and search text */
export function filterModels(opts: {
  category?: ModelCategory | "all";
  tier?: HardwareTier | "all";
  search?: string;
  recommendedOnly?: boolean;
  /** If true, include models without any local backend. Default: false (only show usable models). */
  includeNonLocal?: boolean;
}): ModelEntry[] {
  let results = MODEL_CATALOG;

  // Show all models with at least one backend (Ollama, vLLM, or HuggingFace link)
  if (!opts.includeNonLocal) {
    results = results.filter((m) => !!m.ollamaId || !!m.vllmId || !!m.huggingFaceId);
  }

  if (opts.category && opts.category !== "all") {
    results = results.filter((m) => m.category === opts.category || m.tags.includes(opts.category!));
  }

  if (opts.tier && opts.tier !== "all") {
    const tierOrder: HardwareTier[] = ["tiny", "light", "medium", "heavy", "ultra"];
    const maxIdx = tierOrder.indexOf(opts.tier);
    results = results.filter((m) => tierOrder.indexOf(m.tier) <= maxIdx);
  }

  if (opts.recommendedOnly) {
    results = results.filter((m) => m.recommended);
  }

  if (opts.search) {
    const q = opts.search.toLowerCase();
    results = results.filter(
      (m) =>
        m.name.toLowerCase().includes(q) ||
        m.family.toLowerCase().includes(q) ||
        m.id.toLowerCase().includes(q) ||
        m.tags.some((t) => t.includes(q)),
    );
  }

  return results;
}

