/**
 * Comprehensive open-source LLM model catalog.
 * Used by the agent creation wizard for model selection.
 *
 * Each entry includes real-world specs: parameter count, VRAM needs (Q4 quant),
 * context window, strengths, and hardware tier classification.
 */

// ── Types ────────────────────────────────────────────

export type ModelCategory =
  | "chat"
  | "code"
  | "vision"
  | "image"
  | "embedding"
  | "audio"
  | "multimodal"
  | "classifier"
  | "reasoning";

export type HardwareTier =
  | "tiny"       // 2-4 GB VRAM — phone, RPi, CPU-only laptop
  | "light"      // 4-8 GB VRAM — integrated GPU, old laptops
  | "medium"     // 12-24 GB VRAM — RTX 3090/4090, M1/M2 Pro
  | "heavy"      // 48-80 GB VRAM — A6000, dual 4090, M2 Ultra
  | "ultra";     // 80+ GB VRAM — A100/H100, multi-GPU clusters

export interface ModelEntry {
  /** Unique slug — used as the model ID when pulling */
  id: string;
  /** Human-readable display name */
  name: string;
  /** Model family grouping */
  family: string;
  /** Primary capability */
  category: ModelCategory;
  /** Additional categories */
  tags: string[];
  /** Parameter count string — "7B", "70B", "8x7B" etc. */
  params: string;
  /** Context window in tokens */
  context: number;
  /** Minimum VRAM in GB (Q4 quantization or int8 for small models) */
  vramGb: number;
  /** Recommended quantization */
  quant: string;
  /** Hardware tier classification */
  tier: HardwareTier;
  /** What it's great at — 2-4 bullet points */
  strengths: string[];
  /** Known weaknesses — 1-2 bullet points */
  weaknesses: string[];
  /** License type */
  license: string;
  /** Release date (YYYY-MM) */
  released: string;
  /** Ollama model name (if available) */
  ollamaId?: string;
  /** vLLM / HuggingFace model ID (if available) */
  vllmId?: string;
  /** HuggingFace model ID for direct link */
  huggingFaceId?: string;
  /** Whether this is an editorial recommendation */
  recommended?: boolean;
}

